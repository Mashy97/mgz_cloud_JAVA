// 비교 연산자에 대한 예제
public class Operator02 {

	public static void main(String[] args) {
		int num01 = 5, num02 = 10;
		
		boolean result = num01 == num02;
		System.out.printf("result = %b%n",result);
		
		result = num01 != num02;
		System.out.printf("result = %b%n",result);
	
		result = num01 > num02;
		System.out.printf("result = %b%n",result);
		
		result = num01 >= num02;
		System.out.printf("result = %b%n",result);
		
		result = num01 < num02;
		System.out.printf("result = %b%n",result);
		
		result = num01 <= num02;
		System.out.printf("result = %b%n",result);
	
	
	}

}
