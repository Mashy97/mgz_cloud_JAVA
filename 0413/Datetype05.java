//기본 자료형 - 문자형
public class Datetype05 {
	public static void main(String[] args) {
		 // System.out.println(Integer.toBinaryString(65));
		char ch01 = 'A';
		System.out.println("ch01 = "+ch01);
		
		char ch02 = 65; //ascii code 'A'
		System.out.println("ch02 = "+ch02);
		
		char ch03 = 97;	//ascii code 'a'
		System.out.println("ch03 = "+ch03);
		
		char ch04 = '\u0061'; //unicode 'a'
		System.out.println("ch04 = "+ch04);
		
	}
}
