//양자택일 if문에 대한 예제
public class If02 {
	public static void main(String[] args) {
		
		boolean flag = false;
		
		if (flag) {
			System.out.println("참입니다.");
		} 
		else {
			System.out.println("거짓입니다.");
		}
	
			if (!flag) {
				System.out.println("참입니다.");
			} 
			else {
				System.out.println("거짓입니다.");
			}
		}
}
