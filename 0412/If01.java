// if문에 대한 예제
public class If01 {
	public static void main(String[] args) {
		
		boolean flag01 = false;
		boolean flag02 = true;

		
		System.out.println("Start_01");
		
		if(flag01){
			System.out.println("참");
		}
		System.out.println("END_01");
		
		System.out.println("Start_02");
		if(flag02){
			System.out.println("참");
		}
		
		System.out.println("END_02");
	}
}
